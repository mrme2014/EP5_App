package com.ntk.util;

public class RecSizeItem  {
	
	public String Name;
	public String Size;
	public String FrameRate;
	public String Index;
	
	public RecSizeItem(String name, String size, String frameRate, String index) {
		this.Name = name;
		this.Size = size;
		this.FrameRate = frameRate;
		this.Index = index;
	}
}